
plaka_kod = int (input ("Lütfen plaka kodu giriniz."))

if plaka_kod == 34:
    print ("Girmiş olduğunuz 34 plakası İstanbul iline aittir")

elif plaka_kod == 28:
    print("Girmiş olduğunuz 28 plakası Giresun iline aittir")

elif plaka_kod == 54:
    print("Girmiş olduğunuz 54 plakası Sakarya iline aittir")

else:
    print ("Girmiş olduğunuz plaka kodu sistemde bulunmamaktadır.")




